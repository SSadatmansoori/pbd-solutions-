public class Hello {
    public static void main(String[] args) {
    
    int x = 0;
    while (x < 5) {
        System.out.println("Hello"); 
        x += 1; 
    } 
    
    System.out.println("-----------"); 
    
    
    for (int y = 0; y < 5; y += 1) {
        System.out.println("Hello from for loop");
        
    }
    
    
    
    
    
    
    
    
    
    
   }
}
  
